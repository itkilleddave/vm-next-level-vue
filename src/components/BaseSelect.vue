<template>
    <div class="field">
        <label v-if="label">{{label}}</label>
        <select @input="updateValue" :value="value" v-bind="$attrs">
            <option 
                v-for="option in options" 
                :value="option"
                :key="option"
                :selected="option == value"
            >
                {{ option }}
            </option>
        </select>
    </div>
</template>

<script>
    export default {
        inheritAttrs: false,
        props: {
            options: {
                type: Array,
                required: true
            },
            label: {
                type: String,
                default: ''
            },
            value: [String, Number],
            // placeholder: {
            //     type: String,
            //     default: ''
            // },
        },
        methods: {
            updateValue(event) {
                this.$emit('input', event.target.value)
            }
        },
    }
</script>

